-- Description: Fas 1 av kundrapporten — paketerar website_snapshots-tidsserien till
--              ett kundvänligt månadsmail (trend + en skräddarsydd upsell) bakom en
--              godkännande-grind: cronen skapar en DRAFT per kund och pingar Discord;
--              mailet skickas först efter godkännande (Discord-länk eller CRM-knapp).
--              Fylls av edge-funktionen generate_monthly_reports (manuellt via Kund-
--              fliken eller månadsvis via pg_cron) och skickas av send_monthly_report.
--              Helt additiv migration — inga DROP/ALTER på befintliga objekt.

CREATE TABLE IF NOT EXISTS "public"."monthly_reports" (
  "id" bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "company_id" bigint NOT NULL REFERENCES companies(id) ON DELETE CASCADE,
  -- Rapportperioden som första dagen i månaden (2026-06-01) → (company_id, period) unikt
  "period" date NOT NULL,
  -- Snapshot som rapporten bygger på + den föregående (för trend). SET NULL så att
  -- en raderad snapshot inte tar rapporten med sig (historiken bevaras).
  "snapshot_id" bigint REFERENCES website_snapshots(id) ON DELETE SET NULL,
  "previous_snapshot_id" bigint REFERENCES website_snapshots(id) ON DELETE SET NULL,
  -- draft: skapad av cron/manuellt, väntar på godkännande
  -- approved: godkänd i CRM, på väg att skickas
  -- sent: mailet skickat till kund
  -- failed: AI-validering eller utskick misslyckades (se error)
  -- skipped: ingen snapshot/mottagare → inget att rapportera
  "status" text NOT NULL DEFAULT 'draft'
    CHECK (status IN ('draft', 'approved', 'sent', 'failed', 'skipped')),
  "recipient_email" text,
  "recipient_name" text,
  -- AI-genererad, kundvänd text (validerad mot monthlyReportContentSchema innan spar)
  "ai_content" jsonb,        -- { greeting, summary, recommended_action, upsell_pitch }
  -- Valda upsells (alla matchande sparas; mailet visar den högst prioriterade)
  "selected_upsells" jsonb NOT NULL DEFAULT '[]',
  -- Fryst trend-data så rapporten är reproducerbar även om snapshots ändras
  "metrics" jsonb,           -- { clicks: {current, previous, deltaPct}, ... }
  "generated_html" text,     -- färdig mail-HTML (källa vid utskick + förhandsgranskning)
  "public_url" text,         -- Fas 2: länk till full HTML-rapportsida i Storage
  "approval_token" uuid NOT NULL DEFAULT gen_random_uuid(),
  "email_send_id" bigint REFERENCES email_sends(id) ON DELETE SET NULL,
  "error" text,
  "approved_by" bigint REFERENCES sales(id) ON DELETE SET NULL,
  "approved_at" timestamp with time zone,
  "sent_at" timestamp with time zone,
  "created_at" timestamp with time zone DEFAULT now() NOT NULL
);

