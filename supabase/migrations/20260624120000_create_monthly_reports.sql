-- ============================================================================
-- Månadsvis automatisk draft-generering via pg_cron + pg_net
-- ============================================================================
CREATE EXTENSION IF NOT EXISTS pg_cron;

-- SECURITY DEFINER-funktion som anropar generate_monthly_reports i cron-läge.
-- Läser supabase_url + cron_secret från vault (samma mönster som
-- run_monthly_website_snapshots). Saknas något → varna och hoppa över.
CREATE OR REPLACE FUNCTION public.run_monthly_reports()
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO ''
AS $$
DECLARE
  edge_function_url text;
  cron_secret text;
BEGIN
  SELECT decrypted_secret INTO edge_function_url
  FROM vault.decrypted_secrets
  WHERE name = 'supabase_url'
  LIMIT 1;

  SELECT decrypted_secret INTO cron_secret
  FROM vault.decrypted_secrets
  WHERE name = 'cron_secret'
  LIMIT 1;

  IF edge_function_url IS NULL OR cron_secret IS NULL THEN
    RAISE WARNING 'run_monthly_reports: missing supabase_url or cron_secret in vault — skipping run';
    RETURN;
  END IF;

  PERFORM net.http_post(
    url     := edge_function_url || '/functions/v1/generate_monthly_reports',
    body    := jsonb_build_object('cron', true),
    headers := jsonb_build_object(
      'Content-Type', 'application/json',
      'x-cron-secret', cron_secret
    ),
    timeout_milliseconds := 10000
  );
END;
$$;

-- Kör 07:00 den 1:a varje månad — en timme efter monthly-website-snapshots (06:00)
-- så att den färska snapshoten finns när rapporten genereras. cron.schedule är
-- idempotent på jobbnamn.
SELECT cron.schedule(
  'monthly-reports',
  '0 7 1 * *',
  $$SELECT public.run_monthly_reports();$$
);
